// UN/LOCODE Port Data for Shiportrade.com
// Reference: https://unece.org/cefact/unlocode

export interface Port {
  unLoCode: string;           // 5-character UN/LOCODE
  name: string;               // Port name
  country: string;            // Country name
  countryCode: string;        // ISO 3166-1 alpha-2 country code
  region: string;             // Geographic region
  coordinates: {
    lat: number;
    lng: number;
  };
  portType: PortType[];
  facilities: string[];
  vesselTypes: string[];
  throughput?: string;        // Annual TEU (if applicable)
  timezone: string;
  status: 'active' | 'inactive' | 'limited';
}

export type PortType = 
  | 'Seaport'
  | 'River Port'
  | 'Inland Port'
  | 'Airport'
  | 'Dry Port'
  | 'Container Terminal'
  | 'RoRo Terminal'
  | 'Bulk Terminal'
  | 'Tanker Terminal'
  | 'Cruise Terminal'
  | 'Ferry Terminal'
  | 'Multi-Purpose';

export interface Country {
  code: string;               // ISO 3166-1 alpha-2
  name: string;
  region: string;
  hasCoastline: boolean;
  majorPorts: number;
}

export interface Region {
  id: string;
  name: string;
  description: string;
  countries: string[];
}

