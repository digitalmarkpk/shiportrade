// Helper functions
export function getPortByUnLoCode(code: string): Port | undefined {
  return PORTS.find(p => p.unLoCode.toLowerCase() === code.toLowerCase());
}

export function searchPorts(query: string): Port[] {
  const q = query.toLowerCase();
  return PORTS.filter(p => 
    p.name.toLowerCase().includes(q) ||
    p.unLoCode.toLowerCase().includes(q) ||
    p.country.toLowerCase().includes(q) ||
    p.countryCode.toLowerCase().includes(q)
  );
}

export function getPortsByCountry(countryCode: string): Port[] {
  return PORTS.filter(p => p.countryCode.toLowerCase() === countryCode.toLowerCase());
}

export function getPortsByRegion(region: string): Port[] {
  return PORTS.filter(p => p.region.toLowerCase() === region.toLowerCase());
}

export function getMajorPorts(limit: number = 20): Port[] {
  return PORTS
    .filter(p => p.throughput && p.throughput.includes('M+ TEU'))
    .slice(0, limit);
}

export function getCountryByCode(code: string): Country | undefined {
  return COUNTRIES.find(c => c.code.toLowerCase() === code.toLowerCase());
}

