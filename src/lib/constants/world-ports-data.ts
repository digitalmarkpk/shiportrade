// Export function to get all ports flattened
export function getAllPorts(): Port[] {
  const ports: Port[] = [];
  WORLD_COUNTRIES.forEach(country => {
    ports.push(...country.seaPorts, ...country.airports, ...country.dryPorts);
  });
  return ports;
}

// Export function to get ports by type
export function getPortsByType(type: 'Seaport' | 'Airport' | 'Dry Port' | 'Inland Port' | 'River Port'): Port[] {
  return getAllPorts().filter(port => port.portType.includes(type));
}

// Export function to get ports by country
export function getPortsByCountry(countryCode: string): Port[] {
  const country = WORLD_COUNTRIES.find(c => c.code === countryCode);
  if (!country) return [];
  return [...country.seaPorts, ...country.airports, ...country.dryPorts];
}

// Export function to get country by code
export function getCountryByCode(code: string): CountryData | undefined {
  return WORLD_COUNTRIES.find(c => c.code === code);
}

// Export function to search ports
export function searchPorts(query: string): Port[] {
  const lowerQuery = query.toLowerCase();
  return getAllPorts().filter(port => 
    port.name.toLowerCase().includes(lowerQuery) ||
    port.unLoCode.toLowerCase().includes(lowerQuery) ||
    port.country.toLowerCase().includes(lowerQuery)
  );
}

// Export function to get countries by region
export function getCountriesByRegion(region: string): CountryData[] {
  return WORLD_COUNTRIES.filter(c => c.region === region);
}

// Export function to get statistics
export function getWorldStatistics() {
  const totalCountries = WORLD_COUNTRIES.length;
  const totalSeaPorts = WORLD_COUNTRIES.reduce((sum, c) => sum + c.seaPorts.length, 0);
  const totalAirports = WORLD_COUNTRIES.reduce((sum, c) => sum + c.airports.length, 0);
  const totalDryPorts = WORLD_COUNTRIES.reduce((sum, c) => sum + c.dryPorts.length, 0);
  const totalPorts = totalSeaPorts + totalAirports + totalDryPorts;
  const landlockedCountries = WORLD_COUNTRIES.filter(c => c.coastline === 'Landlocked').length;
  
  return {
    totalCountries,
    totalSeaPorts,
    totalAirports,
    totalDryPorts,
    totalPorts,
    landlockedCountries,
  };
}
