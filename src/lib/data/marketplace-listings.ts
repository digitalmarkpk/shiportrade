// Helper function to get listings by category
export function getListingsByCategory(category: ListingCategory): Listing[] {
  return allMarketplaceListings.filter(listing => listing.category === category);
}

// Helper function to get featured listings
export function getFeaturedListings(): Listing[] {
  return allMarketplaceListings.filter(listing => listing.featured);
}

// Helper function to search listings
export function searchListings(query: string): Listing[] {
  const lowercaseQuery = query.toLowerCase();
  return allMarketplaceListings.filter(listing =>
    listing.title.toLowerCase().includes(lowercaseQuery) ||
    listing.description.toLowerCase().includes(lowercaseQuery) ||
    listing.tags?.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  );
}

