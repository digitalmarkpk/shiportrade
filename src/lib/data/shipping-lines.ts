// Get shipping line by ID
export function getShippingLineById(id: string): ShippingLine | undefined {
  return shippingLines.find(line => line.id === id);
}

// Get shipping lines by country
export function getShippingLinesByCountry(country: string): ShippingLine[] {
  return shippingLines.filter(line => 
    line.country.toLowerCase().includes(country.toLowerCase())
  );
}

// Search shipping lines
export function searchShippingLines(query: string): ShippingLine[] {
  const q = query.toLowerCase();
  return shippingLines.filter(line => 
    line.name.toLowerCase().includes(q) ||
    line.shortName.toLowerCase().includes(q) ||
    line.country.toLowerCase().includes(q) ||
    line.routes.some(r => r.toLowerCase().includes(q)) ||
    line.services.some(s => s.toLowerCase().includes(q))
  );
}

// Get top shipping lines by TEU capacity
export function getTopShippingLines(limit: number = 10): ShippingLine[] {
  return [...shippingLines].sort((a, b) => b.teu - a.teu).slice(0, limit);
}

// Get shipping lines by alliance
export function getShippingLinesByAlliance(alliance: string): ShippingLine[] {
  return shippingLines.filter(line => 
    line.alliances.some(a => a.toLowerCase().includes(alliance.toLowerCase()))
  );
}

